     */
    public function store(Request $request)
    {
        $validatedDataCategory = $request->validate([
            'name' => 'required|string|max:255',
            'slug' => 'required|string|max:255|unique:categories',
            
        ]);

       
        $user = Auth::user();
        $validatedDataCategory['user_id'] = $user->id;
    
        Category::create($validatedDataCategory);
        return redirect()->back()->with('success','category telah di tambahkan');
    }

